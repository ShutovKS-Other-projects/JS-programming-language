// Задание 5: Функции с разными синтаксисами

/*
Создайте функцию, которая принимает два аргумента, выполняет над ними математическую операцию и возвращает ее результат.
Используйте три различных синтаксиса описания функции.
 */

function add(a, b) {
    return a + b;
}

subtract = function (c, d) {
    return c - d;
};

const multiply = (e, f) => e * f;

console.log("Результаты математических операций:");
console.log("Сумма:", add(5, 3));
console.log("Вычитание:", subtract(10, 4));
console.log("Умножение:", multiply(7, 2));
